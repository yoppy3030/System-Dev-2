                    <h2>1. Hiroshima</h2>
                    <ul class="rules-list">

                   <li>
                            <h3>1. Avoid Political Jokes About the Atomic Bomb</h3>
                            <p>In Hiroshima, the memory of the atomic bombing is not just a part of history ‚Äî it's a living, deeply emotional reality for many residents. As such, even casual jokes, political comments, 
                                or lighthearted references to nuclear weapons or war can be seen as deeply insensitive. Locals expect visitors to approach the subject with empathy and seriousness. Making remarks, no matter how harmless they may seem elsewhere, 
                                can cause discomfort or offense. When visiting memorials or speaking with locals, it's important to show respect for the city's tragic past and its ongoing message of peace.</p>
                        </li>
                    
                        <li>
                            <h3>2.  Listen Closely to Dialect: Hiroshima-ben    </h3>
                            <p>The local dialect spoken in Hiroshima, known as Hiroshima-ben, is rich, expressive, and distinctly different from standard Japanese. It often sounds strong or even aggressive to outsiders, but it reflects the warmth and honesty of the people.
                                 Phrases like "„Åò„ÇÉ„Åë„Åà" (meaning "so" or "because"), "„Å∂„Å°" (meaning "very"), or "„ÅÑ„Åë„Çì" (meaning "not good" or "don't do that") are commonly used in everyday speech. While it might sound intense, locals don't mean to be rude ‚Äî it's simply their way of speaking.
                                  Visitors are encouraged to listen and appreciate the uniqueness of this dialect, and even trying out a few Hiroshima-ben phrases can help connect more deeply with the community.</p>
                        </li>

                        <li>
                            <h3>3.  Folded Paper Cranes Are Sacred</h3>
                            <img src="./img/paper_cranes.jpg" alt="Hiroshima Orizuru" class="origami-image"><br>
                            <p>In Hiroshima, folded paper cranes ‚Äî known as orizuru ‚Äî are powerful symbols of peace, hope, and remembrance, especially connected to the story of Sadako Sasaki, a young girl who died from radiation after the atomic bombing.
                                 Visitors often bring thousands of these colorful cranes to the Peace Memorial Park as heartfelt offerings. While they may look like decorations, these cranes carry deep emotional weight and personal prayers.
                                  Touching, rearranging, or taking them is considered highly disrespectful. Tourists are expected to observe them quietly and understand their meaning ‚Äî each crane represents a wish for a world without war.</p>
                            <img src="./img/fire_work.jpg" alt="Chiba Festival" class="festival-image">

                        </li>

                        <li>
                            <h3>4. Quiet Respect at Peace Memorial Sites</h3>
                            <p>The Hiroshima Peace Memorial Park is not just a tourist attraction ‚Äî it is a solemn space dedicated to the memory of the victims of the atomic bombing and a symbol of Hiroshima's call for global peace. 
                                Locals and survivors visit the park to mourn, reflect, and pray. As such, visitors are expected to behave with deep respect. Talking loudly, laughing, running around, or taking cheerful selfies in front of memorials like the Atomic Bomb Dome or the Children's Peace Monument can be seen as deeply inappropriate. 
                                Maintaining a quiet, respectful presence honors both the history and the ongoing message of peace that Hiroshima continues to share with the world.<p>
                        </li>
                    </ul>

                    <!-- Okayama Section -->
                    <h2>2. Okayama  </h2>
                    <ul class="rules-list">
                        <li>
                            <h3>1. Respect for the Legend of Momotaro</h3>
                            <p>Okayama is widely known as the birthplace of Momotaro, the legendary Peach Boy hero from Japanese folklore. For locals, Momotaro is more than just a children's story ‚Äî he's a cultural symbol of bravery, kindness, and local pride.
                                 His image appears on everything from souvenirs and trains to statues and sweets like kibidango. While visitors are welcome to enjoy and engage with the story, mocking the tale, making inappropriate jokes, or treating it as merely a funny cartoon can come across as disrespectful.
                                  Showing appreciation for the legend and its significance helps visitors connect more deeply with the spirit of Okayama.</p>
                        </li>
                    
                        <li>
                            <h3>2.  Okayama Castle Etiquette   </h3>
                            <img src="./img/okayama_castle.jpg" alt="Okayama Castle" class="castle-image"><br>
                            <p>Okayama Castle, often called the "Crow Castle" for its striking black exterior, is a proud symbol of the city's history and culture. While it has been reconstructed, it remains a place of quiet appreciation and learning. Visitors are expected to behave respectfully inside the castle grounds ‚Äî
                                 this means speaking softly, not touching exhibits, and avoiding casual or loud selfies in sacred or historical areas. It's also considered poor manners to run or act playfully inside the castle, especially around traditional displays and cultural artifacts. Treating the space like a living museum, 
                                 with curiosity and respect, honors the legacy the castle represents.</p>
                        </li>

                        <li>
                            <h3>3. Manners on the Kibi Plain Cycling Route</h3>
                            <p>The Kibi Plain Cycling Route is one of Okayama's most scenic and culturally rich paths, stretching through tranquil rice fields, ancient shrines, and historic burial mounds. While it's a popular destination for cyclists, it's not a race track or entertainment zone. Locals expect visitors to ride at a calm pace,
                                 avoid blasting music, and show respect to the rural surroundings. Greeting farmers with a nod, slowing down near shrines, and refraining from loud conversations helps preserve the peaceful atmosphere. Treating the route as a shared cultural space ‚Äî not just a tourist activity ‚Äî shows mindfulness and earns quiet appreciation from the community.</p>

                        </li>

                        <li>
                            <h3>4. Reverence for Kibidango („Åç„Å≥„Å†„Çì„Åî)</h3>
                            <p>Kibidango, a soft and slightly sweet dumpling made from millet flour, is one of Okayama's most iconic local treats ‚Äî famously associated with the folk hero Momotaro. While it may look like a simple snack to visitors, kibidango carries cultural significance for the people of Okayama. It represents hospitality, tradition, and the enduring spirit of the Momotaro legend. 
                                Tossing it, making jokes about its texture, or treating it like cheap candy can be seen as disrespectful. Locals appreciate when tourists enjoy kibidango with sincerity, perhaps even learning its connection to the folklore. Eating it with gratitude is more than good manners ‚Äî it's a quiet way to honor local pride.<p>
                        </li>

                       
                    </ul>
