                    <ul class="rules-list">
                        <h2>3. Tottori</h2>
                        <li>
                            <h3>1.Don't Treat the Tottori Sand Dunes Like a Beach</h3>
                            <p>The Tottori Sand Dunes are one of Japan's most unique natural landmarks, stretching along the Sea of Japan with stunning desert-like landscapes. While they may resemble a beach or desert playground, locals view the dunes as a precious and fragile environment.
                                 Activities like shouting, playing sports, digging, or leaving trash are considered highly disrespectful. The dunes are home to delicate ecosystems and are constantly shaped by wind and weather. Visitors are expected to walk carefully, stay on designated paths, 
                                 and treat the area with quiet appreciation. Preserving the natural beauty of the dunes isn't just about following rules ‚Äî it's about respecting a symbol of Tottori's identity.</p>
                        </li>
                        
                        <li>
                            <h3>2.  Manga Manners in Mizuki Shigeru Road</h3>
                                <p>Mizuki Shigeru Road in Sakaiminato is a lively street dedicated to the beloved manga artist Mizuki Shigeru, creator of GeGeGe no Kitaro. The area is filled with bronze statues of yokai (Japanese spirits), manga art, and themed shops ‚Äî attracting fans from all over the world.
                                     While it's a fun and interactive space, it's important to remember that this street is also a place of cultural pride and local daily life. Climbing on statues, blocking pathways for selfies, or behaving loudly in costume can disturb the respectful atmosphere.
                                      Visitors are encouraged to enjoy the area with care, ask for permission when photographing others (especially cosplayers), and treat the works of Mizuki Shigeru with the reverence they deserve.</p>
                        </li>

                        <li>
                            <h3>3. Don't Waste Your Butadon</h3>
                                <p>Obihiro is the proud birthplace of Butadon ‚Äî a delicious rice bowl topped with grilled pork and sweet-savory sauce. This dish isn't just a local specialty; it represents the region's rich agricultural culture and high-quality pork production. For locals, Butadon is comfort food with deep meaning, often linked to family meals and regional pride.
                                     When tourists order Butadon and leave most of it uneaten, it can feel disrespectful ‚Äî especially in a place where people value food deeply and understand the effort behind every ingredient. To show appreciation, visitors should only order what they can finish and take time to enjoy the flavor and care that goes into every bowl.</p>
                        </li>
                        <li>
                            <h3>4.  Respect the "Wagyu Capital"</h3>
                            <img src="./img/wagyu.jpg" alt="Tottori Wagyu" class="wagyu-image"><br>
                                <p>Tottori takes great pride in its wagyu heritage, often overlooked but deeply respected among beef connoisseurs. Known as the birthplace of some of Japan's finest black cattle, Tottori produces premium-quality wagyu like Tottori Kuroushi and Olein 55, known for its exceptional marbling and buttery texture. For locals, this isn't just food ‚Äî 
                                    it's the result of generations of careful breeding and craftsmanship. When enjoying wagyu in Tottori, it's best to avoid comparing it to more famous varieties like Kobe or Matsusaka beef. Even light comments suggesting one is better than the other may come off as dismissive. Instead, 
                                    expressing genuine appreciation for the unique flavor and local pride behind Tottori wagyu shows cultural sensitivity and respect for what the region has perfected.</p>
                    </ul>
                    <!-- Yamaguchi  Section -->
                    <h2>4. Yamaguchi </h2>
                    <ul class="rules-list">
                        <li>
                            <h3>1. Don't Run or Climb on the Kintai Bridge</h3>
                            <p>The Kintai Bridge in Iwakuni is one of Japan's most iconic wooden bridges, famous for its elegant five-arch design and centuries-old craftsmanship. While it may seem like a fun structure to explore, locals take its preservation very seriously. Running, jumping, 
                                or climbing on the arches is not only dangerous but also deeply disrespectful to a structure that symbolizes local pride and history. The bridge has been rebuilt and maintained with traditional techniques, making it more than just a sightseeing spot ‚Äî it's a living piece of heritage.
                                 Visitors are expected to cross it slowly, take in the view with care, and treat it with the same respect as a historic monument.</p>
                        </li>

                        <li>
                            <h3>2. Fugu Etiquette in Shimonoseki</h3>
                            <p>Shimonoseki is Japan's top producer and culinary capital of fugu ‚Äî the famously delicate and potentially poisonous blowfish. In this city, fugu isn't just exotic food; it's a symbol of refined skill and deep culinary tradition. Only licensed chefs are allowed to prepare it, often after years of training. 
                                Because of its dangerous reputation, some tourists are tempted to make jokes or light-hearted remarks about dying or taking risks while eating it. However, in Shimonoseki, such comments are seen as disrespectful to the chefs and the culture surrounding fugu. Locals expect diners to approach the experience with appreciation and quiet confidence, 
                                recognizing the care and technique behind every slice.</p>
                        </li>

                        <li>
                            <h3>3.Quiet Participation in the Lantern Festivals</h3>
                            <img src="./img/latern_festival.jpg" alt="Yamaguchi Lantern Festival" class="lantern-image"><br>
                            <p>Yamaguchi's lantern festivals, especially the Tanabata Lantern Festival, are serene and spiritual events that light up the streets with soft, glowing beauty. During these festivals, thousands of handmade lanterns line roads and temple grounds, creating a peaceful and almost sacred atmosphere. 
                                While it's natural to want to take photos, pushing through crowds, using flash, or loudly chatting with friends can disrupt the mood that locals work hard to preserve. These festivals are not loud celebrations ‚Äî they are moments of reflection, tradition, and quiet beauty. Visitors are encouraged to walk slowly, 
                                observe silently, and fully absorb the gentle magic of the lantern-lit night.</p>
                        </li>
                        <li>
                            <h3>4.Show Respect at Ruriko-ji Temple and Mori Clan Sites</h3>
                            <p>Ruriko-ji Temple in Yamaguchi is home to one of Japan's most beautiful five-story pagodas and is closely tied to the legacy of the Mori Clan, a powerful family that once ruled much of western Japan. These sacred sites are not just tourist attractions ‚Äî they are places of deep historical and spiritual importance. 
                                Visitors should avoid loud conversations, casual selfies, or playful behavior within temple grounds and especially near the Mori family graves and memorials. Instead, a quiet, respectful presence is appreciated, whether you're walking through the mossy paths, viewing the pagoda, or offering a silent prayer.
                                 Showing reverence in these spaces honors both local culture and centuries of heritage.</p>
                        </li>
                        
                        
