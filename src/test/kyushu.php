            <h2>4. Miyazaki </h2>
            <ul class="rules-list">
                <li>
                    <h3>1. Respect for Shinto Mythology Sites</h3>
                    <p>Miyazaki is deeply connected to the roots of Japanese mythology, with sacred locations like Takachiho Gorge, Amano-Iwato Shrine, and Udo Shrine tied to ancient legends about the gods, including the sun goddess Amaterasu.
                         These aren't just sightseeing spots ‚Äî they are spiritual places where locals come to pray, reflect, and honor their cultural heritage. Visitors should remain quiet, avoid disruptive behavior, and dress modestly when visiting these sites.
                          Taking loud selfies, ignoring shrine etiquette (like bowing or washing hands), or treating the myths as mere folklore can come across as disrespectful. Understanding and honoring the spiritual meaning behind these places allows for a much deeper and more respectful connection to Miyazaki's cultural soul.</p>
                </li>

                <li>
                    <h3>2. Surfing with Aloha Spirit</h3>
                    <p>Miyazaki is a renowned surfing destination, celebrated for its scenic beaches and perfect waves. However, surfing here is not just about riding waves ‚Äî it's about embracing the Aloha spirit, which emphasizes respect, humility, and community. When visiting local surf spots, such as Aoshima Beach or Kushima,
                         it's important to follow surf etiquette. This includes waiting your turn, respecting local surfers, and avoiding aggressive behavior in the water. The vibe is laid-back, with an emphasis on camaraderie and harmony, rather than competition. Being considerate of others and appreciating the natural beauty of the surroundings will ensure a positive experience and demonstrate respect for the local surfing culture.</p>
                </li>

                <li>
                    <h3>3. Respect the Nature and Spiritual Energy</h3>
                    <img src="./img/miyazaki.jpg" alt="Miyazaki Nature" class="miyazaki-image"><br>
                    <p>Miyazaki is known not only for its stunning landscapes but also for its powerful spiritual energy, especially in places like Aoshima Island, Udo Shrine, and the surrounding coastlines. Locals believe these natural spots are filled with sacred power ‚Äî known as power spots („Éë„ÉØ„Éº„Çπ„Éù„ÉÉ„Éà) ‚Äî where people go to pray, heal, or feel connected to something greater. While the scenery may tempt visitors to explore freely or pose for dramatic photos,
                         it's important to stay on marked paths, avoid climbing sacred rocks, and keep noise to a minimum. These areas are treated with quiet reverence, not just as tourist spots but as spiritual sanctuaries. Respecting the natural and mystical essence of these places helps preserve both the environment and the spiritual atmosphere that makes Miyazaki so unique.</p>
                </li>

               
