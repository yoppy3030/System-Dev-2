            <h2>1. Fukuoka</h2>
            <ul class="rules-list">

                <li>
                    <h3> 1. Don't Stay Too Long at Yatai (Street Food Stalls)</h3>
                    <p>Yatai, or street food stalls, are one of Fukuoka's most beloved cultural experiences, offering warm food and friendly conversation in a small, casual setting. However, seating is limited and turnover is important ‚Äî these stalls are designed for quick, social meals, not long dinners. After enjoying your food and perhaps a short chat, 
                        it's polite to make space for others waiting to sit. Lingering too long, especially during busy evening hours, can unintentionally inconvenience both the stall owner and other customers. Being mindful of your time at the yatai helps keep this special tradition alive and welcoming for everyone.</p>
                </li>

                <li>
                    <h3>2. Don't Eat or Drink Around Dazaifu Tenmangu Shrine Grounds</h3>
                    <img src="./img/dazaifu.jpg" alt="Dazaifu Tenmangu Shrine" class="shrine-image"><br>
                    <p>Dazaifu Tenmangu is one of Fukuoka's most important and sacred shrines, dedicated to the deity of learning, Sugawara no Michizane. While the approach to the shrine is lined with lively shops selling sweets and souvenirs ‚Äî including the famous ume mochi rice cakes ‚Äî once you pass through the torii gate, it's important to shift your behavior. 
                        Eating or drinking inside the shrine grounds is considered disrespectful, as the area is a place for prayer, reflection, and quiet. Be sure to finish any snacks or drinks before entering, and always bow respectfully when passing through sacred spaces. Keeping this etiquette helps preserve the solemn atmosphere that worshippers and locals deeply value.</p>


                </li>
                <li>
                    <h3>3. Don't Imitate or Interrupt Hakata Gion Yamakasa Participants</h3>
                    <p>The Hakata Gion Yamakasa is one of Fukuoka's most iconic and sacred festivals, held every July for over 770 years. During this event, teams of men dressed in traditional attire carry massive wooden floats and race through the streets of Hakata in a powerful display of teamwork, speed, and devotion. While tourists are encouraged to watch and enjoy,
                         it's important to respect the spiritual and cultural significance of the event. Never try to join the runners, mimic their actions, or step into their path for photos. Doing so not only disrupts the flow of the procession but also shows disrespect toward the local community that trains and prepares all year for this festival. Enjoy the energy from a safe distance and honor the tradition by observing quietly and respectfully.</p>
                </li>
                <li>
                    <h3>4. Don't Confuse Mentaiko with Common Spicy Paste</h3>
                    <p>In Fukuoka, mentaiko ‚Äî spicy cod roe ‚Äî is more than just a condiment; it's a beloved local delicacy with a deep cultural identity. Unlike generic spicy sauces or pastes, Fukuoka's mentaiko is carefully marinated in a secret blend of chili, sake, and seasonings, with each shop offering its own unique flavor profile. Tourists sometimes mistake it for just another "spicy spread," but doing so overlooks its richness, subtle heat, 
                            and importance in Hakata cuisine. Whether it's served with rice, in pasta, or even on baguettes, mentaiko is meant to be appreciated like fine cheese or artisan miso ‚Äî not dismissed as something ordinary. Trying it with an open mind shows respect for local food culture and the generations of craftsmanship behind each bite.</p>
                </li>
            </ul>

            <h2>2. Oita</h2>
            <ul class="rules-list">
                <li>
                    <h3>1.Don't Treat Onsen Like Pools or Spas</h3>
                    <p>≈åita, especially cities like Beppu and Yufuin, is famous for its incredible variety of hot springs ‚Äî 
                        but these are not the same as Western-style spas or swimming pools. For locals, onsen are places of healing, relaxation, 
                        and tradition, and they come with long-established etiquette. Visitors should never splash, swim, or dive into the water.
                         Before entering, you must wash and rinse your body thoroughly at the provided shower area. Towels should never go into the bath itself ‚Äî
                          leave them on the side or place a small towel on your head. Speaking quietly is also expected, and using phones or taking photos is usually forbidden. 
                          Respecting these customs ensures that everyone ‚Äî locals and tourists ‚Äî can enjoy the peaceful onsen experience together.</p>
                </li>

                <li>
                    <h3>2. Don't Be Surprised by Mixed-Gender Baths</h3>
                    <p>In some rural areas of ≈åita Prefecture, especially in traditional or historic hot springs, you may encounter mixed-gender baths, known as konyoku (Ê∑∑Êµ¥).
                         While uncommon in most parts of Japan today, konyoku is still practiced in certain regions of ≈åita where it's seen as part of the local culture, not something strange or inappropriate.
                          These baths often have modesty rules ‚Äî such as towels, bathing wear, or gender-separated times ‚Äî but the atmosphere remains relaxed and respectful. If you're uncomfortable,
                           simply ask the staff if there's a private or gender-specific option available. Most importantly, avoid reacting loudly, laughing, or staring ‚Äî treating the experience with quiet respect honors the traditions of the local community
                </li>

                <li>
                    <h3>3. Don't Touch Bamboo Crafts Without Permission</h3>
                    <img src="./img/bamboo_craft.jpg" alt="Bamboo Crafts" class="bamboo-image"><br>
                    <p>≈åita, especially Beppu, is renowned for its traditional bamboo craftsmanship, with finely woven baskets, trays, and decorative items that often take days or even weeks to create by hand. These crafts are not mass-produced souvenirs ‚Äî they are delicate works of art that reflect generations of skill and care.
                         When visiting bamboo craft shops, galleries, or exhibitions, it's important to look with your eyes, not your hands, unless signs say it's okay. Many items are fragile or made for display only, and touching without permission could damage them or be considered disrespectful. If you're interested in a piece,
                          politely ask the staff for help ‚Äî they'll usually be happy to show it to you properly.</p>
                </li>

                <li>
                    <h3>4. Don't Take Photos Inside Onsen Facilities</h3>
                    <p>In ≈åita's famous onsen towns like Beppu and Yufuin, photography inside bathing areas is strictly discouraged ‚Äî and often outright prohibited. Onsen are places of privacy, relaxation, and personal comfort, and taking photos can make other guests feel uncomfortable or exposed, especially since everyone is bathing nude.
                         Even in outdoor baths or scenic hot spring areas, if people are present, it's best to avoid using cameras or smartphones altogether. Some sightseeing spots, like the "hells" (jigoku), allow photos ‚Äî but bathing areas do not. Always check for signs, and when in doubt, ask staff. Respecting others' privacy helps preserve the calm, 
                         welcoming spirit of the onsen experience. </p>
                </li>

                <li>
                    <h3>5. Don't Waste Local Foods Like Toriten or Dangojiru</h3>
                    <p>≈åita is proud of its regional dishes like toriten (crispy chicken tempura) and dangojiru (miso soup with hand-rolled dumplings and vegetables), both rooted in local home cooking and tradition. These meals are more than just food ‚Äî they represent the heart of ≈åita's culinary identity. When dining in local restaurants or inns, 
                        it's considered respectful to finish what you're served, or at least try most of it. Leaving food uneaten, especially after showing strong curiosity to try it, may come across as wasteful or impolite. If you're unsure about the portion size or flavor, it's best to order small and add more later. Showing appreciation ‚Äî 
                        even with a simple "gochisousama deshita" after the meal ‚Äî goes a long way in honoring local hospitality.
                    
                </li>
            </ul>
            <ul class="rules-list">
                <h2>3. Okinawa </h2>
                <li>
                    <h3>1.Don't Ignore Umbrella Etiquette on Rainy Days</h3>
                    <p>Okinawa may be part of Japan today, but it was once the independent Ryukyu Kingdom with its own language, culture, and traditions. This unique heritage is still deeply felt by the local people. 
                        Assuming that Okinawa is culturally identical to mainland Japan ‚Äî or treating it like just another tropical tourist spot ‚Äî can come off as ignorant or disrespectful. Locals are proud of their distinct identity,
                         which blends Ryukyuan roots with a mix of Japanese and American influences. Visitors who take the time to learn about Okinawa's history, listen to its music, taste its traditional food, and appreciate its differences are warmly welcomed. Showing curiosity and respect goes a long way in connecting with the Okinawan spirit.</p>
                </li>

                <li>
                    <h3>2. Show Respect When Drinking Awamori</h3>  
                    <img src="./img/awamori.jpg" alt="Awamori" class="awamori-image"><br>
                    <p>Awamori is Okinawa's traditional distilled liquor, made from long-grain Thai rice and aged in clay pots for a smooth, rich flavor. It's more than just a drink ‚Äî it's a cultural symbol often shared during celebrations,
                         family gatherings, and even spiritual rituals. When offered awamori, it's polite to receive it with both hands and return the gesture by pouring for others. Chugging it like a shot or treating it as a party drink can be seen as disrespectful,
                          especially in more traditional settings. Locals value the slow, social nature of drinking ‚Äî it's about connection, not speed. Savoring awamori and honoring the custom of mutual hospitality reflects genuine appreciation for Okinawan culture.</p>
                </li>

                <li>
                    <h3>3. Don't Walk on Sacred Utaki Sites</h3>
                    <p>In Okinawa, utaki are sacred sites deeply connected to the island's indigenous Ryukyuan spirituality. These places ‚Äî often groves, caves, or stone formations ‚Äî are considered dwellings of gods and ancestral spirits. 
                        Many utaki are still actively used for prayer and rituals, especially by local women known as noro (priestesses). Tourists may come across these sites in forests, near beaches, or even along hiking trails.
                         While they may look like natural landmarks, stepping into them, touching altars, or taking casual photos can be seen as highly disrespectful. When in doubt, observe from a distance and follow local signs or guidance.
                          Showing reverence to these quiet spiritual spaces reflects true respect for Okinawa's living traditions.</p>
                </li>
                <li>
                    <h3>4.  Be Sensitive About War History</h3>
                    <p>Okinawa was the site of one of the most devastating battles of World War II, and the scars of that conflict are still deeply felt by its people. The Battle of Okinawa claimed over 200,000 lives ‚Äî including many civilians 
                        ‚Äî and left lasting trauma on the island. Today, places like the Okinawa Peace Memorial Park, Himeyuri Monument, and Former Japanese Navy Underground Headquarters stand as solemn reminders of that tragedy.
                         Visitors are expected to approach these sites with quiet respect and reflection. Making jokes, taking cheerful selfies, or making light comments about war or the U.S. military presence can deeply offend locals. Understanding the pain and resilience behind Okinawa's peace message is key to appreciating the island beyond its beaches and beauty.</p>
                </li>
            </ul>
